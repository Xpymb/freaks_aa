namespace Freaks.Dal.Common.ValueObjects;

/// <summary>
///     Режим отслеживания изменений сущностей при выполнении запросов к базе данных.
///     Используется при работе с Entity Framework для управления трекингом.
/// </summary>
public enum EntityTrackingType
{
    /// <summary>
    ///     Включено отслеживание изменений сущностей.
    ///     Позволяет изменять и сохранять полученные из базы объекты.
    /// </summary>
    Tracking = 1,

    /// <summary>
    ///     Отключено отслеживание изменений.
    ///     Используется для повышения производительности при чтении данных без намерения их изменять.
    /// </summary>
    NoTracking = 2,
}